    <h2 class="title-tds">React</h2>
    <p class="text">
        Criado dia 29 de maio de 2013, o React é uma biblioteca Java Script de código aberto com o foco em criar as interfaces de usuário também conhecido como frondend em páginas web. <br>
         Como componente do Java Script foi desenvolvido pelo Facebook.<br>
         Atualmente, ela permanece na frente das suas principais competidoras, como a Angular e a Bootstrap, as duas bibliotecas Java Script mais bem vendidas. <br>
         O React é a biblioteca mais popular do Java Script.
         Ela oferece uma resposta excelente para o usuário adicionar comandos usando um novo método de renderizar sites. <br>
         Objetivo dela é configurar um ambiente local de desenvolvimento React, criar o primeiro app e entender o básico de como as coisas funcionam.<br>
         É mantido pelo Facebook, Instagram, outras empresas e uma comunidade de desenvolvedores individuais. <br>
         É utilizado nos sites da Netflix, Imgur, Feedly, Airbnb, SeatGeek, HelloSign, Walmart entre outros. <br>
         Em 2015, o Facebook anunciou o módulo React Native, que possibilita o desenvolvimento de aplicativos para Android e iOS utilizando componentes de interface de usuário nativos de ambas plataformas, sem precisar recorrer ao HTML. <br>
         A ideia do React Native é tornar possível criar aplicações mobile nativas com uma única linguagem (no caso, o Java Script), permitindo o reaproveitamento de boa parte do código. <br>
         Isso permite que qualquer um com conhecimento em React já saiba criar componentes no React Native. <br>
         Outro grande recurso é o Hot Reload, que atualiza o aplicativo assim que você alterar um código, poupando o tempo gasto em novas recopilações. <br>
         Pré-requisitos que é bom você ter é familiaridade com HTML, CSS, e linguagem JavaScript, conhecimento de terminal/linha de comandos.
         React usa a sintaxe “HTML in JavaScript” com o nome JSX (JavaScript e XML). <br>
         Familiaridade em ambos HTML e JavaScript ajudará aprender JSX, e identificar melhor se os erros na sua aplicação estão relacionados ao JavaScript ou na parte especifica do React.
