    <h2 class="title-tds">Biblioteca</h2>
    <p class="text">Este trabalho foi produzido para a matéria de Programação Web 3, com a intenção de criar uma biblioteca JS, a que escolhemos foi: React.<br>
        Agora fica a seguinte pergunta, qual o motivo para usar React?<br>
        O React tem sido usado por grandes companhias ao redor do mundo. Como já citado algumas delas: Netflix, Airbnb, American Express, Facebook, eBay e Instagram. Essa é a prova de que a ferramenta tem um número de vantagens que não têm nem comparação nos seus competidores.


     <h2 class="title-tds">Fácil de Usar</h2>
 <p class="text">O React é uma biblioteca de código aberto para interfaces gráficas (GUI) que tem como foco uma só coisa: tornar a experiência do usuário com a interface mais eficiente. <br>
    Ela pode ser categorizada como o “V” no padrão MVC (Model-View-Controller).
Como um desenvolvedor de JavaScript, vai ser fácil para você entender o básico do React.
Para fortalecer seu entendimento, dê uma explorada em alguns dos tutoriais já existentes sobre o React. <br>
Eles contêm muita informação de como usar a ferramenta, como vídeos, tutoriais e dados que podem enriquecer seus conhecimentos.</p>

     <h2 class="title-tds">Suporte a Componente Reusável em Java</h2>
<p class="text">O React permite que você reuse componentes que tenham sido desenvolvido em outras aplicações e que usem a mesma função. A função de reusabilidade é uma vantagem importante para desenvolvedores em geral.</p>

     <h2 class="title-tds">Componente Fácil de Escrever</h2>
<p class="text">Também conhecido como React JS, o componente do React é fácil de escrever porque ele usa JSX, uma extensão de sintaxe opcional para JavaScript. Ela permite que você combine HTML com JavaScript. <br>
O JSX é uma excelente combinação de JavaScript e HTML. Ela simplesmente simplifica toda a estrutura de codificação escrita de um site. Além disso, a extensão também faz com que a renderização de múltiplas funções seja mais fácil. <br>
Ainda que o JSX não seja a extensão de sintaxe mais popular, ela tem provado ser bastante eficiente. Especialmente quando falamos em desenvolver componentes especiais ou aplicações de alto rendimento.</p>

     <h2 class="title-tds">Melhor Desempenho com Virtual DOM</h2>
<p class="text">O React melhora, de maneira eficiente, o processo de atualização do DOM (Document Object Model). Como você deve saber, esse processo pode causar muita frustração em projetos baseados em aplicações-web. <br>
 Por sorte, o React usa Virtual DOMs, então você consegue driblar esses problemas.
A ferramenta permite que você construa um Virtual DOM e o hospede na memória. <br>
Como resultado, toda vez que acontece uma mudança no DOM real, o virtual se modifica imediatamente.
O sistema vai impedir que o DOM real faça atualizações constantemente. Por conta disso, a velocidade da sua aplicação não vai ser interrompida.</p>

    <h2 class="title-tds">Amigável a SEO</h2>
<p class="text">O React permite que você crie uma interface de usuário que pode ser encontrada e acessa em diversos motores de busca. <br>
    Esse recurso é uma vantagem sensacional porque nem todos os frameworks de JavaScript são amigáveis a SEO.
Junto a isso, como o React melhora o processamento da sua aplicação, ele também pode melhorar os resultados do seu SEO.<br> Afinal, o tempo de carregamento de um site é um fator crucial na otimização de SEO.
Porém, você deve se atentar ao fato de que o React é apenas uma biblioteca JavaScript. <br>
Isso significa que você não pode fazer tudo com ele. O uso de bibliotecas adicionais podem ser necessárias para propósitos de gerenciamento, apontamento ou interações complexas.</p>

